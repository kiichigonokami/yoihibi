			<span><h3 class="article_detail_contents_box__title"><span class="kihonnoki_title_cap2">A</span>「有機JAS規格」は「見える化」されているということ</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　「有機JAS規格」では、原則、農薬・化学肥料は使用できませんが、やむをえない場合に限って使用が認められているものがあります。また、有機の加工食品は、使用できる添加物が限定されています。いずれも、情報公開がされていますので、「見える化」されているということです。<br>
 <br>
　一般の加工食品も、商品パッケージの裏面に原材料などが表記されていますが、着色料や増粘剤等の言い回しもできるため、成分を特定することができないものもあります。<br>
 <br>
　<u>自分や家族の口に入る食品の安全性をより高めたいと考えた場合、オーガニックのものを選ぶという行動が健康を守ることにもつながるでしょう。</u>
</p>
