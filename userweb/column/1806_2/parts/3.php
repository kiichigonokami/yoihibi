			<ul>

			  <li class="text_ver">
				<p>
　穀類、野菜、果物、茶などの農産物やその加工食品について、国が定めた「有機JAS規格」のルールを守って作り、認証を受けたものだけが、「有機JASマーク」を付けることができ、商品名にも「オーガニック」「有機」の言葉を使うことができます。<br>
 <br>
　<u>天然、農薬不使用であっても、認証を受けていなければ、オーガニックを名乗ることはできません。</u>オーガニックと有機は同義で使われます。
 </p>
			  </li>
