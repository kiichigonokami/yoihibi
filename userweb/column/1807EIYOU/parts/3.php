                                <span><h3 class="article_detail_contents_box__title">近年注目される「シナリン」</h3></span>
                                <ul>
                                  
                                  <li class="text_ver">
                                    <p>　アーティチョークは、特に葉の部分にポリフェノールの一種であるシナリンを多く含みます。シナリンは、海外では肝機能を高める胆汁分泌促進剤や高コレステロール血症の治療にも使われます。<br>
<br>
　シナリンの独特の苦みが胆汁の分泌を促し、肝臓の負担をやわらげたり、肝細胞の回復を促進したりします。これが、古くから二日酔いに効くと言われる所以です。また、胆汁の分泌が多くなることで消化が促進されるため、夏バテなどの食欲不振時の改善に有効です。ほかにも、コレステロール値や中性脂肪値を下げる、利尿作用、便秘や生活習慣病の改善にも効果があるとされています。<br>
<br>
　シナリンの研究は、国内外で活発になってきています。アーティチョークは今後ますます有望なハーブと言えるでしょう。<br>
<br>
＊キク科植物にアレルギーのある方は、注意が必要です。</p>
