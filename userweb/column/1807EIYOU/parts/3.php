<b>アーティチョーク</b><br>
● 英語名：アーティチョーク<br>
 （和名：チョウセンアザミ）<br>
● 地中海沿岸地方から中央アジア原産<br>
　 キク科チョウセンアザミ属<br>
<br></p>
                                  </li>
                                  <li class="text_ver">
                                    <p>　キク科の多年草であるアーティチョークは初夏に、15センチと大人の手のひらほどもある紫色の花を咲かせ、草丈も、大きいものでは2メートル近くになります。名前の語源となったアラビア語のアルカルチェフが「大きなアザミ」を意味する通り、アザミの花がそのまま巨大化したような姿は、初めて見る人を驚かせます。<br>
<br>
　日本ではあまりなじみがなく、イタリアンやフレンチのレストランなどでたまに見かける高級な野菜というイメージですが、欧米では日常的な野菜で、花が咲く前のつぼみの部分を丸ごと蒸したり茹でたり、半分に切ってオーブンで焼いたりして食べます。　アーティチョークは水溶性の食物繊維を豊富に含んでいます。水溶性食物繊維は便秘の改善に有効で、腸内の調子を整え、糖質や脂質の吸収を抑えます。そのほかに、葉酸、ビタミンＣ、カロテン、カリウム、鉄分、マグネシウム、カルシウムといった栄養素も含みます。<br>
<br>
　アーティチョークの葉や根を乾燥させたものは、古代ギリシア・ローマ時代より「メディカルハーブ」として珍重されており、現在でも世界各地でハーブティーなどとして生活に根付いています。</p>
                                  </li>
                                  
                                </ul>
