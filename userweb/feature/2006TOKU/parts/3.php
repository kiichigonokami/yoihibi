<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl4">免疫と再生の仕組みが回復力に影響する</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　ウイルスの特徴を知るにつれて絶望的な気分になりそうですが、希望が持てるのは新型コロナウイルスに感染しても回復する人が大勢いるという点です。重症化する人と回復する人の違いについては今後の科学的な検証を待ちましょう。<br>
<br>
　「免疫」を考える上で分けることができない「再生」という体の仕組みを知ると、私たちにも今からできることがもっと見つかりそうです。実際、再生医学は免疫学抜きで考えることができない分野で、多くの研究者が注目しています。<br>
<br>
　ウイルスの弱点は、細胞の中でしか増殖できないことです。不思議なことに私たちの免疫は、「ウイルスに感染した細胞が自ら死を選ぶ」ことも選択肢として用意しています。細胞自身が死ねば、結果的に、細胞内のウイルスも無力になるわけです。なんだかもの悲しい選択肢ですが、私たち自身を守るための免疫反応で起こる神秘的な一面と言えます。<br>
<br>
　このとき問題になるのは、自ら死を選ぶ細胞の数が多すぎると、その細胞でできた臓器が機能低下して生死を分ける可能性もあるということです。<span class="marker">自死する細胞の数、そのタイミング、そこに新しく健康な細胞を再生させること──私たちの生存にとってはこの絶妙なバランスがとても大切</span>になります。<br>
<br>
　新型コロナウイルスに感染し重症化してしまった場合に使われる呼吸補助療法にエクモ（ECMO、体外生命維持装置）があります。これは、治療機器ではなく、ウイルス感染のせいで機能しなくなった肺の機能を一時的に代行する装置です。装置を使う間に医療でできるのは、唯一、患者さん自身の力で肺の機能が回復するのを待つこと。日本では52%の回復率が報告されています（２０２０年３月24日時点）。約半数が回復するのは、免疫力と再生力のバランスがうまく働いた結果です。<br>
<br>
<br>

				</p>
			</li>

