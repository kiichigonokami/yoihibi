<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl5">免疫力と再生力を下げてしまうものとは？</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　私たちを外敵から守るための免疫、そして外敵と戦ったあとに回復するための再生という仕組みですが、気をつけなければ自分で免疫力や再生力を下げてしまいます。<br>
<br>
・<span class="marker2">多すぎる加工食品</span><br>
　体が排除しなければならないような、小さな外敵を増やさない食生活が普段から大切です。それには、人工的で不自然な添加物がたくさん使われている加工食品を食べ過ぎないことです。<br>
<br>
・<span class="marker2">砂糖や悪い油の多い食事</span><br>
　糖分の多い食事や飲み物、フライ・揚げ物・油炒めなどが中心の食事は、血流を悪くさせやすく、白血球の動きをさまたげます。また、糖分過剰で血糖値が不安定だと、体のエネルギー供給が不安定になるので再生力を発揮しにくくなります。　<br>
<br>
・<span class="marker2">野菜が少ない食事</span><br>
　免疫細胞がもっとも多く集中するのは食べ物が通過する腸管周辺で、その環境を整えているのは腸内細菌です。腸内環境を良く保つ一つのコツは、食物繊維を適度に摂ること。そのためにも野菜が必要です。<br>
<br>
・<span class="marker2">歯周病など歯のトラブル</span><br>
　歯周病など歯の感染症を放置するのは体にとって大きなストレスになり、免疫の働きを下げます。歯科医による定期的なクリーニングや点検をしましょう。<br>
<br>
・<span class="marker2">長期間続く心理的ストレス</span><br>
　慢性的な心理ストレスをその根本原因から解決するのは難しいことです。だから長期化しているのです。でも、これが免疫の働きを悪くするのも事実です。原因そのものを取り除けないなら、それについて脳を使う時間を予め区切り、考えない時間を確保しましょう。その時々に最優先するべきことに集中する習慣が付くと、心理的ストレスを減らすのに役立ちます。<br>
<br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl6">免疫力を強化する栄養成分</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　免疫も再生も、その仕組みを動かすのは「力」です。どちらにとっても実行力としてのエネルギーが安定的に必要です。私たちのエネルギー源の中心は複合炭水化物や脂肪なので、穀物や野菜がバラエティー豊かに含まれる食事が大事です。また、免疫細胞や組織再生の材料としてのたんぱく質も必要です。<br>
<br>
　こうした三大栄養素を、自然に安全に育てられた新鮮なホールフーズ（精製加工されていない食材）から食べることが、免疫と再生の仕組みを動かす力に直結します。それらの食品には加工によって失われがちな微量栄養素も含まれるので、免疫細胞の働きを強化し、再生のための材料と動力を供給します。<br>
<br>
　そのほか、あらゆる栄養成分が私たちの免疫と再生を支えます。<span class="marker">何か一つの栄養成分だけでなく、自然な食材に含まれるすべての成分を取り入れることが、免疫力と再生力に繋がります。</span>難しく考える必要はありません、あなたが自分で作る食事に「希望」がたくさん詰まっているということなのです。<br>
<br>
<br>

				</p>
			</li>

		</ul>
</div>


