<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title" id="toku-ttl1">免疫力とは、何ですか？</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　免疫力とは、文字通り、疫病を免れる力のことで、流行感染症の原因菌やウイルスなど、外から入ってくる病気の原因に対する抵抗力も免疫力の一つです。厳密には、<span class="marker">別の生物に自分が侵略されないよう「自己と非自己を区別して排除する仕組み」を免疫、その仕組みを動かす力を免疫力</span>と言います。この仕組みを中心的に担うのはさまざまな種類の白血球で、免疫細胞とも呼ばれます。<br>
<br>
　白血球は、私たちの体内に自分以外の生物が入ると相手の情報をリサーチし、その情報をもとに戦略を練り攻撃します。免疫は一度攻撃したターゲットを記憶し、同じ敵が二度目の侵入をすると、過去の経験からより速く撃退します。ワクチンは免疫のこの仕組みを利用した医療手段です。<br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl2">「新型コロナウイルス」が手強い理由</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　昨年末に中国の武漢から全世界に広がり、多くの感染者と死者を出している新型コロナウイルス。このウイルスの感染拡大を私たち人類がなかなか食い止められないのは、「新型」だからというのが大きな理由の一つです。<br>
<br>
　<span class="marker">これまで人類が出会ったことがない新顔なので、私たちも、そして私たちの免疫も、ウイルスに関する「情報」がありません。</span>そのため、抗ウイルス薬やワクチンの治療法も用意がなく、私たちの免疫も速攻攻撃に出られないのです。<br>
<br>
　しかし、そんな新顔のウイルスであっても外から入ってきた非自己なので、私たちの免疫は必死で抵抗し排除を試みます。少なくとも特効薬ができるまでの間は、私たち自身の免疫力が最大の命綱です。<br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl3">謎の多いウイルスという存在</h3></span>
	<ul>

		<li class="text_ver">
			<p>
　元来、ウイルスというのは謎が多く、私たちの細胞の表面にくっつくと細胞の方から細胞膜でそのウイルスを包み込み、自分の内部に入れてしまうという反応が起こります。細胞内ではその後、ウイルスがDNAやRNAゲノムを細胞内部に吐き出し、コピーを作って増殖します。このコピー作業は細胞内でしかできません。<br>
<br>
　つまり、ウイルスの存続と繁栄は、相性の良い細胞との出会いに依存しています。<span class="marker">新型コロナウイルスと相性の良い細胞は、鼻や口の粘膜、眼の結膜、気管、気管支、肺などの細胞と考えられている</span>ので、まずはマスクや手洗い、うがいなどでウイルスがこの周辺の細胞に接触しないようにするのが第一です。<br>
<br>
　もちろんウイルスが細胞に入った後も免疫は必死の抵抗を試みますが、それが功を奏さずウイルスが細胞内で増殖すると、１細胞あたり何百、何千、何万個にも及ぶウイルス粒子となって細胞の外に解き放たれます。そしてそれぞれが新たな細胞との出会いを求めて体内をさまようのです。<br>
<br>
<br>

			</p>
