			<span><h3 class="article_detail_contents_box__title">大地の鼓動がいざなう“人助けのお水”</h3></span>
			<ul>

			  <li class="text_ver">
				<p>
　『月のしずく』と『神秘の水 夢』のふるさと、天然温泉施設「ゆの里」は、和歌山県橋本市神野々（このの）に、昭和62年創業しました。<br>
<br>
　「ゆの里」の始まりは「これからは進んで健康を守らなければならない時代が来る。入浴施設を作って人々の健康のお役に立ちたい」という創業者の思い立ちから。専門家の見立てでは、「この地に水脈はない」というものでしたが、それでも井戸を掘って欲しいと希望を伝えると、その通り、50メートル掘っても水の気配がなかったところに、折りも折り、橋本市に震度４の局所地震が起きます。地震から一夜、水脈がないはずの場所に、清らかな水がこんこんと湧き出したのでした。平成２年、今度は同じ場所の地下１１８７メートルに、岩盤が抱えるようにして眠っていた温泉水を発見します。<br>
<br>
　後からわかったことですが、高野山が開かれるさらに100年前、神野々一帯には3万坪ほどのお寺があったそうです。さらには「この地の下には人助けの水がある」との言い伝えがあったとか。<br>
