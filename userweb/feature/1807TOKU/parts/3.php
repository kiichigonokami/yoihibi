			<span><h3 class="article_detail_contents_box__title">地下水（金水）、温泉水（銀水）、２つのお水の特性が活きている
</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　ほどなくして、お水の効果を実感したお客さまから体験談や感想、感謝の言葉が寄せられるようになり、口伝えで評判が広がっていきました。<br>
<br>
　地震で現れた地下水は「金水」と呼ばれ、湧出量が豊富なだけでなく、大腸菌はもちろん、あって当然の一般細菌も含まれなかったという、保健所も驚いたお水です。<br>
<br>
　一方、「銀水」と呼ばれる温泉水は、黒色片岩という硬い岩盤に、35億年前の太古よりカプセル化されていた、いわば有限の化石水と、当時、専門家から言われました。一般的に水は空気に触れたそばから劣化が始まりますが、この温泉水（銀水）は、汲み上げ後１～２年経っても変質しません。また、ミネラルの種類も量も類いまれなほどに富んでいます。<br>
<br>
　この温泉水（銀水）を地下水（金水）で希釈し、最良のバランスに仕立てたものがミネラルウォーター『月のしずく』です。<br>
