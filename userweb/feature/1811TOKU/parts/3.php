				<p>
　遠赤外線にはまったく熱はありません。では、どうして『アルファウェーブ』を身につけると温かいのでしょうか？ 人体から出ている遠赤外線の波長と『アルファウェーブ』の波長が「共振運動」を起こすことで、遠赤外線が増幅し、熱が生まれるという仕組みです。<br>
<br>
　それぞれの波長は、同じ約10ミクロンであることが重要で、同じ波長同士であるからこそ、からだの中から温めることができます。ですから電気やガスなどの熱源はまったく必要ありません。<br>
<br>
　例えば熱した石から出る遠赤外線で焼く石焼き芋は、皮は焦げずに中までホカホカ、これが『アルファウェーブ』の温め方です。
