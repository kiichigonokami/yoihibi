<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title" id="toku-ttl4">一人ひとりの行動が環境を守る一歩になる</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　さらに、健康を守るためにも、オーガニックはおすすめです。「国産は安心」と思っている方が多いかもしれませんが、<span class="marker">実は慣行農場の単位面積あたりの農薬使用量は、日本は世界でトップクラス</span>と言われています。その点、<span class="marker">オーガニックであれば、国産・外国産を問わず、厳しい基準をクリアしている</span>ことが「見える化」されていますから、広い視野で商品を選択するのも一つの方法です。<span class="marker">環境にやさしいということは、結果的に、私たちの健康にも負担が少なく、自然の力で育った作物の風味もまた格別です。</span><br>
<br>
　最近では有機専門店も増え、スーパーや百貨店のオーガニックコーナーも広がってきています。皆さんには、一ヵ月に一品でもオーガニックの商品を選ぶ、オーガニックが欲しいとお店で声をかける、などの行動を起こしてもらえるとうれしいです。<br>
<br>
　どうぞ皆さんの生活の中で、オーガニックに触れる機会を増やしていただきたいと思います。<br>

