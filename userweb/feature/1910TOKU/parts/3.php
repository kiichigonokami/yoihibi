		<span><h3 class="article_detail_contents_box__title" id="toku-ttl1">農業は一万年以上前から、環境に負荷をかけ続けている</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　農耕の起源は一万年以上前と言われています。昔の農業は現在のように化学肥料や農薬がなかったので、今でいうオーガニック（有機）に近いものだと思われがちですが、環境への配慮という観点からみると、決してそうではありませんでした。農業推進のために無秩序な開発を行った結果、文明を滅ぼしたり、中世ヨーロッパでは大規模な森林破壊が引き起こされました。<br>
<br>
　時代が下り、化学肥料や農薬の使用が始まったのは１５０年ほど前のことです。日本で広く普及したのは、第二次世界大戦後、ＧＨＱの推進がきっかけで、農家を重労働から解放し、収穫量を飛躍的に伸ばしました。<br>
<br>
　一方、１９５０年前後にアメリカで窒素肥料によって硝酸態窒素を過剰に含む野菜を食べた赤ちゃんが酸欠状態になった「ブルーベビー症候群」、また、ベトナム戦争で散布された強力な除草剤・枯葉剤が原因の土壌汚染による長期の健康被害等が社会問題になりました。そして、レイチェル・カーソン著『沈黙の春』や有吉佐和子著『複合汚染』等が出版され、化学肥料や農薬使用に対して疑問をもつ人々が有機農業を始める動きが出てきました。<br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl2">オーガニックの本来の目的は、環境保護</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　有機農業が広がるにつれ、にせ物や粗悪品が流通するようになり、それに対して、アメリカやヨーロッパでは、民間の団体によるいくつもの認証制度が作られました。その後、有機農産物の国際平準化が図られ、日本でも有機JAS認証制度がスタートします。<br>
<br>
　農林水産省の定める有機農産物の日本農林規格第2条には、「農業生産に由来する環境への負荷をできる限り低減した栽培管理方法を採用した圃場（農産物を育てる場所）において生産すること」とあり、<span class="marker">有機の本来の目的は「環境保全」ということが示されています。「健康にいい」「おいしい」ものを作ることが一番の目的ではありません。</span><br>
<br>
　その具体的な農法とは、例えば山芋を栽培する際、慣行農法では病気を防ぐために殺菌剤を土壌に入れます。一方、有機の土壌は、土そのものに力がありますが、種をまく前にマルチシートという黒いビニールシートを敷いて太陽熱で殺菌させます。そのシートは後から回収するので土壌汚染はありません。その他、稲作に使用する回収の手間がいらない溶ける紙製のシートもありますが、化学処理がされているため有機では使えません。このように、<span class="marker">肥料や農薬だけでなく、道具の使用に関しても法律によって細かく定められています。</span><br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl3">有機がもたらす生物の多様性</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　私が検査員として有機農家を訪問するとき特に印象的なのは、有機の畑や田んぼと慣行農法のそれらとの見た目の違いです。<br>
<br>
　慣行農法の田んぼは雑草もほとんどなく一見きれいに整っています。それに対して、有機の畑や田んぼは、場所にもよりますが、作物とともに草花も生い茂っています。例をあげると、北海道では、他ではほとんど見られない絶滅危惧種の花が一面に咲いていました。また、生き物調査をすると、トンボやカエル、ザリガニ等もたくさん生息していることが確認できます。<span class="marker">本来の自然の姿の中で農業を営んでいる有機農家は、作物を作るという経済活動と同時に環境保護活動もしていることになります。</span><br>
<br>

			</p>
