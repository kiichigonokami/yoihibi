					<b>アレルギーコップとは</b><br>
					花粉や食品などのアレルギーの原因物質、環境汚染による有害な化学物質、外食や加工食品に入っている食品添加物などが体内に蓄積していくと、ストレスなどによる免疫力低下に伴い、アレルギー反応が現れることを、「コップから水があふれる」という例えで表現しています。
