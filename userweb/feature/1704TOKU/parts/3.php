<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title">食生活でできる３つの対策</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　以前より佐野先生は、アレルギーの原因になっているものを「入れない」「溜めない」「出す」という３ステップの対策を推奨されています。今月号では３ステップの働きをより高め、コップに余裕を作るために食生活でできることをご紹介します。<br>
<br>
<span class="text_ver_b">&#9312; 質の良い油を摂る</span><br>
　有害物質は「肥満細胞」に溜まります。質の悪い油を摂取すると肥満細胞の質が悪くなり、細胞膜が上手く機能しなくなります。細胞内外の物質の出し入れがしにくくなり、細胞の中に有害物質は溜まりやすくなります。代謝も悪くなるので太りやすくなり、溜める場所を自ら増やすという悪循環に陥ります。質の良い油を適量摂り、汚れが溜まりにくい細胞を作ることが大切です。<br>
<br>
<span class="text_ver_b">&#9313; 腸内環境を整える</span><br>
　便秘などによって腸内環境が悪くなると、腸の粘膜が壊れて穴があき、本来は体内に入れない食品添加物などの有害物質が、容易に体内に流れ込みます。毎日の良質な便通は大人のアレルギーを予防するためには欠かせません。<br>
<br>
<span class="text_ver_b">&#9314; 内臓に負担をかけない</span><br>
　最近ではご高齢の方も洋食を好まれますが、肉や乳製品は消化に時間がかかり内臓の負担が大きいので、和食中心の食生活をおすすめします。また、加工食品を摂取したり偏食したりしても、消化に大きな負担がかかり、ビタミンやミネラルが不足して代謝も低下します。<br>
<br>

				</p>
			</li>

		</ul>
</div>

