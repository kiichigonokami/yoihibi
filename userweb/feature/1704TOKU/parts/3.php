		<span><h3 class="article_detail_contents_box__title">大人のアレルギーとは？</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　近年、子供の頃はアレルギーとは無縁だったのに、大人になってから突然食物アレルギーになる人が増えています。なぜそのようなことが起きるのでしょうか？<br>
<br>
　「アレルギーの原因になりやすい食品を長年摂り続けると、ＩｇＥという抗体が過剰に作られからだに溜まります。これが、アレルギー反応が起こる原因です。特に、大人の場合は小麦、果物、甲殻類などが原因になっているケースが多くみられ、何となく調子が悪い、だるい、疲れが取れないなど、一見するとアレルギーとは関係のなさそうな不調がじわじわと現れ、しかも治りにくいのが特徴です」<br>
<br>
　また、佐野先生は食品と一緒に入ってしまう有害な化学物質の影響も大きいと考えておられます。<br>
<br>
　「自然放牧で健康的な乳牛を育てる酪農家を訪ねた時、乳アレルギーがあると言っていた2名が、搾りたての牛乳をおいしく飲めたということがありました。乳アレルギーではなく、悪さをしていたのは乳牛の飼育をはじめ、牛乳の製造時に使用する薬や化学物質であることが分かり、あらためて食品の選び方について考えさせられました」<br>
<br>
　「からだの中のコップがいっぱいになってあふれるとアレルギー反応が出る」という話を皆さんも聞いたことがあると思います。コップの大きさは人それぞれで、加えて食生活や生活習慣によって溜まるものの種類や量、溜まる速度も異なりますが、現代の生活環境では誰にでもアレルギーが起こる可能性があります。<br>
<br>

				</p>
			</li>

		</ul>
</div>


<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title">コップをいっぱいにしないために</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　「花粉症の場合、薬を飲んでアレルギー反応が出ないように抑え込みます。しかしその間にも、花粉はからだに入ってきてＩｇＥ抗体も溜まり続けるので、薬を止めるとまた同じ反応が現れます。薬で抑え込める量以上のＩｇＥ抗体が溜まってしまうと、症状が現れてしまいます。そうなると、西洋医学では薬の量を増やすしかありません。<br>
<br>
　薬を飲んで症状を抑えながら、一方で花粉を取り込まない、からだの外に排出するなどの対策をしてからだ全体の調子を整えれば、コップの中に余裕ができて症状は出にくくなります。先天的な体質でない限り薬は止めることができます」<br>
<br>
　症状が出るのは悪いことばかりではなく、コップがいっぱいになったという「からだからのサイン」と佐野先生はおっしゃいます。同じものを食べ続けることも、アレルギーのリスクを高めてしまいます。<br>
<br>

				</p>
			</li>

