		<div class="article_detail_contents_box">
				<span><h3 class="article_detail_contents_box__title">脳のエネルギー源にも、必須脂肪酸が必要</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　全身の大多数の細胞と同じく脳細胞は、その中のミトコンドリアでATPというエネルギー源を作り活動しています。このミトコンドリアの膜にも豊富な必須脂肪酸が必要です。脳細胞の活動で最もエネルギーを使うのが神経伝達で、研究ではα–リノレン酸を摂取しないと脳の神経伝導が遅くなることがわかっています（＊２）。脳の構造と機能には脂肪、とくに必須脂肪酸が大切と書いてきましたが、<span class="marker">脳を生かすエネルギー作りにもブドウ糖だけでなく必須脂肪酸が欠かせないのです。</span><br>
<br>
　必須脂肪酸は新鮮な未精製の油から摂取し、一般的には成人の場合、オメガ３は4・5ｇ、オメガ６は11gくらいが目安ですが、現代の食環境ではオメガ６が過剰になりやすいため、脂肪研究者のウド・エラスムス博士は自身が開発したバランスオイルのオメガ３対オメガ６を、２対１に調合し1日のトータルバランスを良くしています。この場合、体重25㎏につき、１日に約12　（大さじ１杯）が適量です（＊５・６）。<br>
<br>
　長年、脳に与える必須脂肪酸の影響を研究してきたシュミット博士は次のように述べています。<span class="marker">「あなたをまるごと変えてしまうのがまさに必須脂肪酸だ」</span>。


