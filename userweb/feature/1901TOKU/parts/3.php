				<ul>

					<li class="text_ver">
						<p>
　コロラド州の研究者マイケルA・シュミット博士は、必須脂肪酸は「割合」が重要と、事故で腹部を手術し小腸と大腸の一部を失った少女の例を挙げています。点滴栄養だけの生活を数ヶ月強いられた少女は一年未満で数多くの神経症状を現し始めました。手足の無感覚、感覚障害、脚の痛み、目のかすみ、手の震え、すべて神経変性の症状です。10分と歩くことさえできなくなりました。血液検査でオメガ３の一種、α-リノレン酸の低下が判明しました。与えられていた点滴栄養を調べると、オメガ３とオメガ６が誤って１対１１５で一年近く投与されており、バランスを１対６に修正すると少女の神経症状は消えました（＊２）。このように必須脂肪酸とそのバランスは、脳と神経系に大きな影響を与えるのです。<br>

