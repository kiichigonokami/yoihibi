		<div class="article_detail_contents_box">
				<span><h3 class="article_detail_contents_box__title">食事の油は、脳に大きな影響を与える</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　私たちのからだはさまざまな栄養でできています。骨にはカルシウム、筋肉にはたんぱく質由来のアミノ酸がそれぞれ重要なことはよく知られます。では、骨や筋肉でできた全身をコントロールする脳に大切な栄養は何でしょうか？ <span class="marker">脳は頭蓋骨のなかで脳脊髄液という液体に浮かびその構造の約60%（特に神経細胞膜の大部分）が脂肪です</span><small>（＊１）</small>。骨のカルシウムや筋肉のたんぱく質が食事から得られるように、脳を作り働かせる脂肪も、妊娠前から死の直前まで食事で摂った油脂に由来します。<br>
<br>
　長い間、食事の油が脳に与える影響は大きくないと考えられてきましたが、現代では少し状況が変わってきました。<span class="marker">食事の油は脳に大きな影響を与え、ふさわしくない油を食べれば脳に問題を引き起こし、適切な油を良い割合で食べれば脳機能を最大限に高めて維持できる</span>ことが世界中の研究でわかってきたからです。とくに、油や脂肪を構成する脂肪酸の研究に注目が集まっています。<br>
<br>
　例えば「血漿中のオメガ３脂肪酸（必須脂肪酸の一種）が低い成人はアルツハイマー病などの認識機能障害と認知症のリスクが高くなる」、「必須脂肪酸（オメガ3脂肪酸とオメガ6脂肪酸）不足の子どもは読解力が低く、つづりや聴覚記憶を間違えやすい傾向がある」という報告があります（＊２）。

						</p>
					</li>

				</ul>
		</div>

		<div class="article_detail_contents_box">
				<span><h3 class="article_detail_contents_box__title">「オメガ3」と「オメガ6」のバランスが重要</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　脳の神経細胞のうち、ミエリンと呼ばれる部分の75%は脂肪でできていて、これが神経伝達のスピードを決めます。この構造が悪ければ脳機能にも影響が出ます。神経細胞は他に、別の細胞との結合部シナプスに全身で最も高濃度のDHA（オメガ３）を持っています。これが不足すれば細胞間の交信に問題が生じます。<br>
<br>
　脳の構造だけでなく、全身に脳からの指令を届けるメッセンジャーもまた必須脂肪酸から作られます。免疫細胞の活動と停止、血管の拡張と収縮などを制御する役目で、オメガ３から作られるプロスタグランジン３、オメガ６から作られるプロスタグランジン１や２といったメッセンジャーがあり、互いにアクセルとブレーキのように正反対のメッセージを届けるので、<span class="marker">調和の取れた脳機能にはオメガ３とオメガ６のバランスが鍵になります。</span><br>
						</p>
					</li>

					<li class="image_ver">
