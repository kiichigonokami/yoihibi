			<ul>


			  <li class="text_ver">
　アルファウェーブを身に着けるとちょうどいい、気持ちのいい温かさだと思いませんか？ それにはちゃんとした理由があります。私たち人間の平熱は36・５～37度ですが、それを遠赤外線の波長に換算すると約10ミクロンになります。アルファウェーブから出ている遠赤外線の波長も約10ミクロンで、同じ波長同士で共振し合って、熱が発生し、からだの中から温まるという仕組みになっています。ですから、からだにとってちょうどいい温かさが持続します。<br>
<br>
　その温かさを保温するために、中綿を入れて、スクエアパッドは５層構造、パイルケットは４層構造にしています。寝具でここまで層を重ねているものは珍しいかもしれません。でも、軽くて薄いので扱いやすいと思います。私は親の代から寝具を扱っていたこともあり、寝具としての使いやすさ、耐久性、耐洗濯性など、細かい点まで気を配って作っています。<br>
