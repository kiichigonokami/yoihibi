			<span><h3 class="article_detail_contents_box__title">質の良い睡眠には「アルファ波」</h3></span>
			<ul>

			  <li class="text_ver">
				<p>
　皆さんは「アルファ波」という言葉を聞いたことがありますか？ 脳波の一つでリラックスしたときに出ると言われています。クラシック音楽の中にはアルファ波が出るものもあるそうですが、その音楽にアルファ波が組み込まれているわけではありません。脳波とは私たち人間のその時の精神状態を表すものです。全部で５種類あり、その中でも日中活動をしているときに出るベータ波とアルファ波のバランスをうまくとることが、心身の健康には大切とされています。<br>
<br>
　例えば、夜休むときにリラックスした状態で布団に入って目を閉じると、アルファ波が出てすっと眠りに入ることができますが、イライラしたり、考え事をしたり、嫌な気分を引きずっているとベータ波が出ているので、なかなか眠りに入ることができません。<br>
<br>
　睡眠は疲労や悪いストレスなどで歪んだ心身を修復し、免疫機能を活性化させるための重要な時間ですが、ベータ波が出たままでは心身の修復ができず明日への元気を蓄えることができません。睡眠の質を高め、健康を維持するためには、アルファ波の存在が欠かせません。
