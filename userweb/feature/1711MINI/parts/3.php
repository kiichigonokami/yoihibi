			<span><h3 class="article_detail_contents_box__title">『アルファウェーブ』を使うと、リラックスして「アルファ波」が出ます</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　アルファウェーブの『スクエアパッド』と『パイルケット』をご愛用の方々には、「熟睡できる、眠りの質が良くなった」と大変喜ばれています。アルファ波はいろいろな条件が揃わないと出ないものですが、アルファウェーブを使用するとアルファ波が出ることが試験によって明らかになっています。左上の図のように動物（犬）による試験では、たった60秒でアルファ波が出てリラックス状態になっています。ペットの犬や猫がアルファウェーブからなかなか離れない、必ずその上で寝るという声をよくお聞きしますが、試験の結果からも納得できます。<br>
<br>
　アルファウェーブは高品質の綿素材を使い、静電気もほとんど発生しませんし、遠赤外線でからだを温める効果もあり、それらの相乗効果でリラックス状態を作っていると考えられます。特に、これから空気が乾燥してくると寝具とパジャマなどの摩擦で起こる静電気は質の良い睡眠にとって大敵になりますが、発売当時から「静電気が起きない」という声をよくいただいています。公的機関による試験でも証明されています。<br>
<br>
　実は、アルファウェーブというブランド名は「アルファ波（ウェーブ）」が由来になっています。人生の３分の１は睡眠時間です。その貴重な時間を明日の健康のために有効に使いましょう、というのがアルファウェーブのテーマです。

