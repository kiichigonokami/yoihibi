<b>日本の皆さまへ</b>
<span>
日本人は歴史を重んじ、祖先に対しての敬意が感じられます。ドイツ、特にバイエルンでも同じことが言えます。例えば、私どもは健康に役立つ薬学（ハーブ）に関して、祖先が残した知恵を活かすべく、日々精進しています。その際、最良で最新のテクノロジーを用い、ハーブの効果を最大限引き出す努力をしております。<br>
