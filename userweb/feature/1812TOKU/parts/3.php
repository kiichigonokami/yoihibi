		<div class="article_detail_contents_box">
			<span><h3 class="article_detail_contents_box__title">今からでも「硬さ」「詰まり」を改善できる</h3></span>
			<ul>


			  <li class="text_ver">
　20代の頃のような若々しく柔軟性がある血管に戻すことは難しいですが、血液がスムーズに流れるように、硬さや詰まりをある程度改善することは可能です。<br>
<br>
　まず、食生活でできることは、良質の油や旬の野菜を意識して食べ、ビタミンやミネラル、からだを酸化から守る抗酸化作用のあるフィトケミカルを摂り入れましょう。そして、睡眠。人間は寝ている間に細胞を修復します。血管の細胞も例外ではありません。また、適度な運動をして、血液を活発に流すことで、血管を鍛えることも必要です。<br>
<br>
　血管に負荷をかけないためには、この時期は内外からからだを冷やさないことや、急激な温度変化を避けること、それから、カッと頭に血が上るような怒り方をしないようにしましょう。キレやすい性格の原因には栄養不足が疑われるケースも多いので、そういう意味でも食生活は大切です。<br>
<br>
　高齢の方でも、血管の老化度は千差万別です。遅すぎることはありませんので、生活習慣や食生活を見直し、若々しい血管年齢を取り戻しましょう。<br>
