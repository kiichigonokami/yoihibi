			<span><h3 class="article_detail_contents_box__title">血流の大事な役目とは</h3></span>
			<ul>

			  <li class="text_ver">
				<p>
　食事から摂った栄養素や肺から入ってきた酸素などは、血液に取り込まれ、血液の流れによってからだのすみずみの細胞まで運ばれます。<br>
そして、それと同時に、各所の細胞から不要になったものを引き取り、便や尿、汗として排出できるよう、それぞれの処理場まで運ぶ役目があります。<br>
<br>
　ですから、血流に問題があると、せっかく栄養価の高いものを食べても、細胞までしっかり届けられない、また、不要なものもきちんと引き取れないということが起こります。<br>
<br>
　スムーズな血液の流れが、からだを正常に機能させるうえでとても大事なことです。<br>
<br>
