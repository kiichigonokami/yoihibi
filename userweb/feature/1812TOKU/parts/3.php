			<span><h3 class="article_detail_contents_box__title">血液の通路である「血管」が老化すると</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　円滑な血流を維持するためには、血液が流れる通路である「血管」の健康状態が大きく関わっています。よく「血管年齢が若い」などと言われますが、これは血管がしなやかで、弾力性がある状態を表しています。逆に、「血管が老化する」というのは、肌と同様に弾力性が低下し、硬くもろくなった状態です。これが「硬化」です。外科医としての経験上、手術時にちょっと持ち上げただけで、ちぎれてしまうほど、硬くもろくなった血管を実際にたくさん見てきました。<br>
<br>
　そして、「硬化」が進むと、血流が悪くなり、「詰まり」を起こしやすくなります。「詰まり」とは、脂肪や血液自体の不要なものなどで血液中に汚れの淀みができると、そこに引っ掛かるようにして汚れが重なっていきます。そして、しだいに血管の内径を狭くします。狭くなったところは、血圧を上げれば無理に押し通すことができるのですが、完全に詰まってしまえば、いわゆる「梗塞」という状態になります。<br>
<br>
　右のチェック表のように、急に血圧が高くなった、手足の末端が冷えやすい、実年齢よりも老けてみえる、などが気になる場合は、血流が悪くなったために、からだのすみずみの細胞まで栄養や酸素が届いていない可能性があり、血管の老化が懸念されます。命に関わる病気のリスクを高めないために、気になる症状を放置せず、病院での検査や日頃の生活を見直すなどの対策を考えましょう。<br>
<br>
<br>
