		<span><h3 class="article_detail_contents_box__title" id="toku-ttl1">腸内細菌に「善」「悪」はない？</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　私たちの腸には、約100兆個、100種類以上もの腸内細菌が生息し、腸の中を顕微鏡でのぞくと「お花畑（flora）」のように見えることから「腸内フローラ」と呼ばれています。これまでは、それらの菌は、からだによい働きをする「善玉菌」、悪い働きをする「悪玉菌」、どちらとも判断できない「日和見（ひよりみ）菌」の３つに分類されると考えられていました。しかし、最近になって研究が進み、<span class="marker">腸内細菌は「善」「悪」に分けられるのではなく、腸内細菌たちのチームワークによって腸内環境の善し悪しが決まる</span>ことがわかってきました。<br>
