<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl5">こんなにすごい！発酵代謝物質「エンザミン」</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　さらに、プレバイオティクスに加えておすすめしたいのが、植物性発酵代謝物質の「エンザミン」です。<br>
<br>
　私が医師として訪れる高齢者施設で、便通でお悩みの方々に、腸内環境を整える効果があるとして販売されているビフィズス菌や乳酸菌のサプリメントを飲んでもらったところ、まったく効果がなかったということがありました。その後、エンザミンを試してみたら、すぐに便通の調子がよくなり、それだけでなく体温が上昇したり、骨密度が上がるという高齢者には嬉しい効果もありました。<br>
<br>
　さらに、<span class="marker">エンザミンには、「プレバイオティクス」としての働きに加え、腸に直接作用し免疫力を高めることでからだ全体のバランスを整える、「バイオジェニックス」の作用もある</span>ことがわかってきて、これからますます注目を集めるでしょう。<br>
<br>
　今の段階では、腸内環境をよくするためには、オリゴ糖、食物繊維、そしてエンザミンがベストミックスと考えています。<br>
<br>

			</p>
		</li>


		</ul>
</div>

<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl6">毎日の生活習慣を見直して、腸内環境を整えましょう</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　今まで腸によい物質についてお話ししてきました。便秘薬を欠かせないシニアの方も多いと思いますが、便秘の原因は食生活による場合も多く、繊維質や水分の不足が挙げられます。また、腸の蠕動が弱くなるので、それを促すための運動が必要です。私が訪れる高齢者施設では、筋トレやエアー縄跳びをしたり、オリーブオイル、根菜や海藻類、きな粉、ココアなどを食べてもらったら、お通じが出やすくなったという方も大勢いらっしゃいます。このようなことを実践しながら便秘薬を減らせるようにしていきます。<br>
<br>
　また、働き盛りの方の便秘は、朝ゆっくり時間を取れていないことも原因の一つでしょう。腸は繊細な臓器で、排便にはタイミングも重要なポイントです。腸が動いて排便できるようになるには起床してから30分くらいはかかりますので、出かけるせめて1時間くらい前に起きて、ゆっくりした時間を作るようにしましょう。<br>
<br>
　よい腸内環境を作るためには日頃の養生も大切です。<span class="marker">日常においては、生活リズムを整え、ストレスをなくし、筋力を落とさない、睡眠を十分とること。食生活では、糖質（糖分）を摂りすぎないようにし、よい油（オメガ3など）、オリゴ糖や食物繊維を意識して摂りましょう。</span><br>
<br>

