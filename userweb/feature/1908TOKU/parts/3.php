<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl3">自分の腸内環境を知る方法は？</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　ここまでのお話で、自分の腸内環境がどのようになっているのか気になっている方もいらっしゃると思います。最近では、検査が可能になり、菌種や数だけでなく、有害菌の増殖を抑える「乳酸産生菌」、抗炎症作用がある「酪酸産生菌」など、腸内環境に効果的な働きが期待できる菌なども検査項目に含まれていて、これらの客観的なデータをもとに腸内環境の状態を知ることができます。<br>
<br>
　検査以外では間接的な判断になりますが、便秘や下痢がある、免疫力の低下、アレルギー反応が出る、アトピー性皮膚炎の悪化などがある場合は、腸内環境が悪いことが疑われます。さらに、薬を長期的に多用している人は要注意で、特に抗生物質は、腸内環境を破壊し、免疫力を低下させて、病気をひき起こすのではないかと懸念されています。<br>
<br>
　<span class="marker">腸はただ食べ物を消化・吸収して排泄するだけの器官ではなく、免疫機能を高め、精神の安定、脳機能の向上などに関与し、全身の健康において重要なバロメーター</span>となっています。便通だけでなく体調の変化にも留意しましょう。<br>
<br>

			</p>
		</li>


		</ul>
</div>


<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title" id="toku-ttl4">「プロバイオティクス」から「プレバイオティクス」へ</h3></span>
		<ul>

			<li class="text_ver">
				<p>
　では、腸内環境をよくするには、どうすればよいのでしょうか。<br>
<br>
　一般に、整腸作用にすぐれたものとしてイメージするのは、ビフィズス菌や乳酸菌でしょう。これらの微生物のことを「プロバイオティクス」と言います。からだの外からよい菌（微生物）を補うという、従来から馴染みのある方法です。しかしながら、この方法は、毎日大量に摂取しない限りほとんどの菌が腸に定着せず、コストがかかる割に、効率がよくないことがわかってきました。<br>
<br>
　その理由は、外から入れた菌と、もともと腸にいる菌の相性が合わず、うまくチームに入れないまま排出されてしまうことが多いからです。<br>
<br>　
　そこで、最近では、腸内細菌の働きを助ける物質＝「プレバイオティクス」を摂ったほうが、効率がよいと考えられるようになりました。それは簡単に言うと、<span class="marker">もともと腸内に存在する菌に栄養を与えて有用菌を増殖・活性化させることで腸内環境を整える</span>という方法です。「オリゴ糖」や「食物繊維」などが代表的なものです。これらは、プロバイオティクスと違い、人によって合う、合わないがなく、効果を感じやすいでしょう。<br>
<br>

				</p>
