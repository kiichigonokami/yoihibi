<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl2">腸内細菌はチームで働きます</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　つまり、悪い働きをする菌がいるのではなく、それぞれの環境下、たとえば、<span class="marker">食事や生活習慣、ストレス、薬の服用、さらに、温度や湿度の変化などによって、多種多様な菌がチームを作って各々の役割を担う</span>ということです。<br>
<br>
　このチーム作りで大事なのは、「多様性」であり「バランス」です。たとえば、100種類の菌があるとして、1種類の菌が99％を占め、他の99種類が１％しかなかったら、バランスが悪くて腸内環境がよい状態とは言えません。野球を例に挙げてみましょう。いくら剛腕ピッチャーがいても、一人では試合に勝てません。9人の選手がそれぞれの役割を果たしてこそ、よいパフォーマンスが生まれます。また、レギュラー選手だけでなく、試合状況やチームの状態にいつでも対応できるように、さまざまなタイプの控え選手がいることも、強いチームになるためには欠かせません。<br>
<br>
　それと同じように、<span class="marker">菌も多種多様でバランスよく存在していれば、腸内環境の変化に対応しやすい</span>ということになります。<br>
<br>

				</p>
			</li>

