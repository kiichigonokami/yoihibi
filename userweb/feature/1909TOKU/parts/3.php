<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl5">入れない工夫、出す対策</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　このように、体内の有害物質が肝臓やその他の解毒器官で対処できる量や質を超えてしまうと、私たちの健康には現実的な問題が発生します。<span class="marker">いちばんわかりやすい対策は有害物質をこれ以上体内に入れないことです。</span>衣（医）・食（職）・住環境の全体を再点検し、よりクリーンな生活環境になるよう工夫しましょう。<br>
<br>
　また、<span class="marker">肝臓が快適に機能できるように整えてあげると解毒はスムーズに運びます。そのためには、肝臓の負担になるような食習慣を見直すことも大切です。</span>たとえば、高脂肪、高精製糖質（白砂糖、精白小麦など）の食事は、コレステロールの合成を促進して肝脂肪の蓄積につながるので、肝機能を悪化させます。断食、拒食症、良質なたんぱく質のない食事が長引くと、肝臓に脂肪が沈着しやすくなり、アルコール摂取の過多も脂肪肝や肝硬変につながります。ほとんどの医薬品も肝機能を抑制してしまうので、不注意に使い過ぎないことが大切です。<br>
<br>

			</p>
		</li>


		</ul>
</div>

<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl6">浄化・解毒を助ける栄養は？</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　肝臓の解毒機能に必要な栄養を食生活で強化するのもまた、解毒をスムーズに進めるポイントです。それにはまず、<span class="marker">未精製や未加工のホールフードを中心に、野菜を多くして、できれば信頼できる農家さんが作っている無農薬・無化学肥料のものやオーガニック栽培で作られた食品を増やすことです。</span>なぜなら、肝臓やその他の器官で有害物質を代謝するには、その化学反応プロセスであらゆる種類のビタミンやミネラルが必要だからです。それには精製されていない健康でクリーンな食べものが持つ多様な栄養成分が必要です。<br>
<br>
　以前は問題がなかったのに、成人してからアレルギーを発症したというような場合は、長年肝臓が同じ種類の有害物質を処理し続けてきたために特定の種類の栄養成分が解毒プロセスで消耗してしまった、ということも考えられます。解毒プロセスでよく使われる栄養成分には、たとえば、セレニウム、マグネシウム、亜鉛、ビタミンＡ、Ｃ、Ｅ、Ｂ３、Ｂ６、Ｂ12、葉酸、アルファリポ酸などがあります。ハーブならマリアアザミ、朝鮮五味子、ターメリックが役に立ちます。野菜なら、ブロッコリー、カリフラワー、キャベツなどのアブラナ科の野菜、そして胆汁を増やす水溶性食物繊維の豊富な食事がよいと言われています。<span class="marker">油脂類は肉類、乳製品などの飽和脂肪酸を少なくして、オメガ３などの必須脂肪酸をバランス良く加熱せずに摂取します。</span>あらゆる工夫で肝臓の荷を軽くすることが全身の浄化には効果的です<br>
