<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title" id="toku-ttl3">体内で分解できないものが、有害物質</h3></span>
	<ul>

		<li class="text_ver">
			<p>

　人類の遠い祖先が地球上に誕生してからヒトに進化するまでにはなかったような数多くの有害物質とともに、今の私たちは生活しています。日々、有害物質を呼吸で吸い込み、飲食で飲み込み、皮膚から吸収しているのに生きていられるのは本来なら驚くべきことですが、私たちのからだは「想定外の有害物質」にもある程度対処できるようになっていて、即死することから免れています。<br>
<br>
　たとえば、工場や自動車から排出される大気汚染物質、芳香剤、喫煙、汚染された水、殺虫剤、合成洗剤、塗料、防腐剤、食品添加物、農薬、薬、化粧品、染料など、細胞にとって有害な物質を数え上げればきりがありません。<span class="marker">これらが有害な理由は、多くは人間が人工的に作り出した合成成分だからで、それを元の自然な成分に戻すプロセスを無視したまま使い続け、体内に入れ続けていて、出すことを考えていないからです。</span>現在世界中で取り組んでいる海洋プラスチック問題と同じで元の自然な成分に分解できないことが問題です。もし、人体内で簡単に自然な成分にまで代謝できるものなら、それは有害物質にはなりません。<br>
<br>

