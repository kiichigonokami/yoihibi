	<span><h3 class="article_detail_contents_box__title" id="toku-ttl2">悪い食習慣は骨が弱くなる!?</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　ただし、酸の生産が過剰になると、肺や腎臓の解毒機能だけでは追い付かなくなります。過剰な酸の原因として、たんぱく質の摂り過ぎ、精製された糖質（白砂糖、精白小麦など）やアルコール類の日常的な摂取などが挙げられます。その場合は、過剰になってしまった酸を緩衝するために、骨からカルシウムが抜け出て、弱アルカリ性を維持することで、pH問題を一時的に解決することがあります。<span class="marker">問題なのはこれを繰り返すと骨は弱くなり、カルシウムが軟組織に溜まって関節炎、動脈硬化、心臓病、血栓、脳梗塞、胆石や腎結石などを招くことです。</span>対策としては、ビタミン・ミネラル・抗酸化物質を豊富に含む野菜や果物を取り入れたバランスのよい食生活と胃腸の消化機能を健全に保つことが大切になります。<br>
<br>

