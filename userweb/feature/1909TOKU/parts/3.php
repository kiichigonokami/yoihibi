<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title" id="toku-ttl4">有害物質はどこから排出される？</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　人体が「想定外の有害物質」にもある程度対処できるのは、多くは肝臓の解毒機能のおかげであり、最終的に全身の解毒の保証をするのは複雑な解毒プロセスを持つ肝臓です。<br>
<br>
　肝臓は解毒の中心的な器官で、有害物質の除去を準備する「体内の下水処理施設」だと考えればわかりやすいかもしれません。たとえば、脂溶性の有害物質は肝臓で水溶性に変えられ、腎臓から尿として、または、肝臓で胆汁と混ぜ合わされて便として、体外へ排泄されます。<br>
<br>
　しかし、肝臓を含めて人体の解毒能力には限界があります。<span class="marker">処理するべき有害物質が多過ぎるか、肝臓の解毒機能に問題が生じれば、未処理の毒は皮膚、粘膜（呼吸器の粘膜、泌尿生殖器の粘膜、胃腸の粘膜）、腎臓、リンパ系を使って体外へ出ようとし、各部位に浮腫や炎症など不快な症状を伴うこともあります。</span><br>
<br>
				</p>
