<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title" id="toku-ttl1">“解毒”は生命活動の一部です</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　有害物質の「解毒」を意味する「浄化」「デトックス」という言葉を日本でもよく見聞きするようになりました。「解毒」だなんて、毒ヘビにかまれたわけでもないのに大げさと思われそうですが、実は私たちは普段から「解毒機能ありき」で生命活動を営んでいます。<br>
<br>
　たとえば、私たちが生きるための生命エネルギーは食事中の炭水化物（糖質）・脂質・たんぱく質から作っていて、これらを何度も化学反応させながら最終的にエネルギーを完成させます。その途中では炭酸などの「酸」を中間産物として大量に発生します。私たちのからだは一部の例外的な場所を除けば「弱アルカリ性（pH７・４）」で最適な健康状態になります。このとき酸の量は０・００１６ミリモル程度。もし、エネルギーを作るたびにできる「酸」を放置すれば１分毎に10・４ミリモルずつ増えます。１分で酸が６５００倍になるのですから、からだの酸性化は急速に進みます。４分経てば強酸性のpH３・０になって細胞は働きを止め、生命活動も停止します。実際にはそうならずに回避できているのは肺と腎臓のおかげです。<br>
<br>
　このように、エネルギーを作るたびに生まれる炭酸などの酸は動物のからだではどうしても作られてしまう「想定内の有害物質」なので、進化の過程でそれを除去する解毒機能も肺や腎臓に用意されています。炭酸は二酸化炭素と水に変えられ、二酸化炭素は肺から、水は腎臓から、それぞれ体外へ除去されます。こうして<span class="marker">肺と腎臓の解毒機能の働きによって細胞は機能を維持し、私たちも生きているのです</span>。<br>
<br>

