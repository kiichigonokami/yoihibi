		<span><h3 class="article_detail_contents_box__title">体調不良を引き起こす「熱」と「湿気」</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　夏は、木々の緑が濃くなり、自然界に活発なエネルギー（陽気）が満ち溢れる季節です。自然の一部である人間も、朝は自然と早く目覚め、夜は遅くまで起きて、精力的に活動することができます。<br>
<br>
　一方で、夏の暑さはからだの中に熱を溜めます。その熱を発散させるために、汗を出し、体温を調節していますが、何らかの理由でその働きがうまくいかずに<span class="marker">からだの中に熱がこもってしまうと、イライラ、頭痛、夏バテ、熱中症などさまざまな体調不良に見舞われます。</span>さらに、熱のエネルギーは上昇する性質があり、最悪の場合、脳梗塞や脳いっ血などの脳の血管の病気を引き起こすこともあります。<br>
<br>
　熱がこもって発散できない原因の一つは、「冷え」です。一日中冷房の効いた室内にいて皮膚表面を冷やしすぎると汗腺が閉じてしまい、からだの中にたまった熱を発散できなくなります。汗をかいたとしても、日本の夏は高温多湿であるため、皮膚表面の汗が蒸発できず、熱と湿気がからだにこもりがちになります。また、冷たい水や食べ物の摂りすぎは、からだの中を冷やすため、内蔵の代謝が落ち、水分の巡りを停滞させます。下痢やむくみも、冷えによる水分の停滞が影響しています。<br>
<br>
　このように、<span class="marker">夏の体調不良には「熱」とともに「湿気」も密接に関係している</span>ことがわかります。<br>
<br>

