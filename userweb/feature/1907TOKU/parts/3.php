				<p>
　よい日々2月号（2019年）でもお話ししましたが、一年を健康に過ごすためには、季節にあった養生が大切です。<span class="marker">春に陽気をからだに蓄え、陽気がピークになる夏は活発に動くことで、熱を発散させることができます。一方で、陽気の熱を抑えるために陰気（水液）を使いすぎて消耗させると、秋、冬の乾燥する季節に、からだの中はカサカサになってしまいます。</span>アトピーなどの皮膚トラブルが悪化するのも、夏の養生が影響しています。<br>
<br>
　昨今の異常なまでに暑い夏を乗り切り、秋に向けて体調を整えるためには、何事もやりすぎは禁物です。動きすぎず、冷やしすぎず、飲みすぎず、あまり無理をしすぎないよう心がけましょう。<br>
