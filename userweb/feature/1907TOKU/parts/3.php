<div class="article_detail_contents_box">
	<span><h3 class="article_detail_contents_box__title">夏の養生で気をつけることは？</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　東洋医学では、からだの中の熱（陽気）を抑える働きをするものを「陰気（いんき）」といい、汗もその一つですが、人間のからだの中にもともと備わっています。それが、血液、髄液（ずいえき）、リンパ液などの水液です。それらの陰気は、汗をかく（発散する）と失われますので、水を飲んだり、食事から補給する必要があります。また、<span class="marker">暑い中、屋外で活動をしているのに汗をかけない方は、陽気が不足し、からだの中まで冷え切っていることが考えられますので、生活習慣を見直しましょう。</span><br>
<br>
　一般には、夏の体調管理として水を多めに飲むことが推奨されていますが、東洋医学では誰にでもおすすめするということはしません。なぜなら、左の表にあるように、不要な水分を体内にため込んでいる「水毒」タイプの方が、のどが渇いていないのに、無理に水を飲んでしまうと、うまく代謝ができずにますます水分を停滞させ、胃腸の働きが悪くなることにより、栄養を吸収できなくなることがあるからです。<br>
<br>
　本来であれば、適度な活動をし、汗をかくことによって、飲む水の量も自然と増えるはずです。まずは、そのような生活を心がけましょう。<br>
<br>

