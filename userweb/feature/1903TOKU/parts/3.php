				<span><h3 class="article_detail_contents_box__title">大病をきっかけに「食養」の世界を知る</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　私が「食養」を指導、自らも実践するようになって60年近くになります。この数十年を通して言えるのは、「今がいちばんからだの調子がいい」ということです。私の歳でこう言うと皆さん驚かれますし、もともと丈夫だったのではないかと聞かれますが、若い頃の私は健康体からほど遠い状態でした。<br>
<br>
　18歳の頃、こはだを食べたのをきっかけに湿疹が出て、病院に通いましたがなかなか治りませんでした。４～５年もの間、温泉療養などさまざまな治療も試みましたが一向によくならず、人からすすめられて断食道場に行きました。すると、断食が功を奏したのか湿疹は治まりましたが、道場から帰ってきた私は、思いもかけず吐血してしまったのです。<br>

						</p>
					</li>

					<li class="image_ver">
						<div class="ima_box"><img src="https://aladdinec.jp/client_info/FLORAHOUSE01/view/userweb/images/feature/1903TOKU/img1.png" alt="" /></div>
					</li>

				</ul>
		</div>

		<div class="article_detail_contents_box">

				<ul>

					<li class="text_ver">
						<p>

　驚いて大学病院にかけこみ検査を受けると、なんと栄養失調で重度の結核に罹っていました。そのときに診てくださった結核病棟の責任者だった方が、その後の私の生き方を変えることになった日野厚先生でした。<br>
<br>
　日野先生は西洋医学の医師ですが、食事でからだを健康にする「食養」の研究にも取り組まれていました。断食等の民間療法にも詳しく、民間療法は現代医学と連携してこそ効果を発揮する、という考えをお持ちでした。日野先生によると、私のからだは断食ができるような状態ではなかった、断食前に検査をしていれば、結核の悪化を防げたはずだと怒っていらっしゃいました。入院は１年にも及びましたが、日野先生のお考えに賛同した私は、退院した次の日から先生のお手伝いを始めました。<br>
<br>

						</p>
