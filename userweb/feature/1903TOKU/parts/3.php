		<div class="article_detail_contents_box">
				<span><h3 class="article_detail_contents_box__title">「食養」で病気を改善する</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　１９６３年、日野先生と共に小田原女子短期大学栄養科学研究所の設立に参画しました。そして、北品川総合病院の勤務を経て、１９７８年から東京・大田区の松井病院食養内科で患者さんの食生活改善指導にあたりました。<br>
<br>
　<span class="marker">食養内科では、現代栄養学の基本は押さえながら、各々の患者さんの病気や体質に合わせて玄米食、魚、大豆、海藻、野菜などを中心とした日本の伝統食の長所を生かした献立を作成していました。</span>からだによい食事を作るためには、吟味した食材、調味料を選びますから、当然、一般の病院食の費用では賄えず、プラスアルファの負担金がかかります。それでも、食養内科を訪れる患者さんの多くは、一般の病院では思うような改善が望めなかったり、西洋医学だけでなく食養での治療も積極的に取り入れたいという方々でしたので、食に対する意識も高く、一生懸命取り組まれていました。食養内科は、現在はなくなりましたが、当時の患者さんとのお付き合いはいまだに続いていて、退院して何十年も経った方から食事についてもよく相談されます。<br>
<br>
　私の周りの元気な高齢者の方々は、自分で、あるいはご家族がきちんと食事を手作りされている方が多いですね。一方、加齢とともに料理をすることが難しくなったり、一人暮らしで、食事を準備するのが億劫になったという声もよく聞きます。そんな時、私はなるべく簡単にできて、毎日続けやすい食事の工夫をアドバイスします。例えば、「ご飯にすりごまと青のりをかけて食べる」、「焼きのりを一枚加える」、「ちりめんじゃこを小松菜のおひたしにかける」などの食べ方をすれば、不足しがちなミネラルを摂ることができます。また、野菜の煮物やけんちん汁を多めに作って小分けしたものを冷蔵庫で保存し、食べるときに味噌や醤油などで味付けをしたり、豚肉や豆腐などを加えたりすれば、味に変化がついて飽きずに食べられますね。<span class="marker">皆さんが住んでいる土地のもの、旬の新鮮な食材を使うのもいいと思います。そして、ゆっくり時間をかけてよく噛んで、腹八分に食べることも大切です。</span>

						</p>
					</li>

				</ul>
