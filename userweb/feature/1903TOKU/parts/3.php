				<span><h3 class="article_detail_contents_box__title">「おだし」でたんぱく質を効率的に摂る</h3></span>
				<ul>

					<li class="text_ver">
						<p>

　そうは言っても、現代生活の中で良質な食材を手に入れ、バランスよく栄養を摂るのは難しいと思います。そんな時にとても重宝するのが、無添加の「おいしいだし」（よい日々ショップでは『スープ・スープ』という商品名で販売しています＊）です。料理のだしや調味料として使ったり、野菜等を入れて栄養スープにしたりして、毎日手軽にたんぱく質やカルシウムなどを摂ることができます。特殊な膜でろ過する段階で<span class="marker">たんぱく質が小さく分解されペプチド化されているので、効率的に栄養が吸収されます。</span><br>
<br>
　私が「おいしいだし」の素晴らしさを知ったのは、アトピーの赤ちゃんに使った時のことでした。たんぱく質アレルギーを起こす赤ちゃんには、徐々にたんぱく質に慣らしていくために、白身魚などを少しずつ与えていくのですが、ほんの少しでも食べる量を増やすと、またアトピーをぶり返してしまうことがたびたびありました。ところが、「おいしいだし」は、アトピーが出ず、しっかりたんぱく質が吸収され、栄養を摂ることができるようになりました。<br>
<br>
　それで、病院の食事にも採用するようになり、さらに退院される患者さんや、老人ホームなどで固形の食事が食べられない方、一人暮らしの高齢者の方々におすすめしたところ、皆さん大変お元気になられています。骨粗しょう症がすっかりよくなられた方もいらっしゃいます。<span class="marker">忙しい、食事を作るのが大変などの食生活のお悩みがある方、栄養バランスの偏り、少食や消化吸収能力の衰えなどを改善したいと思っている方にも広くおすすめしたいですね。</span>
