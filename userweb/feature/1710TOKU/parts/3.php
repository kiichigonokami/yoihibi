		<ul>

			<li class="text_ver">
				<p>

　オーガニックの食品を食べるようになってよかったことは、味の違いが分かるようになったことです。おいしさの感じ方は個人差がありますが、玉ねぎやにんじんは分かりやすいのでおすすめしたいです。オーガニックのものは味がシンプルで雑味がないと思います。紅茶を飲んだら、ふわっといい気持ちになってその場の空間が変わった、という方もいました。<br>
<br>
　私は、自分のアレルギー体験を通して、食べ物から命をいただいていると強く感じます。そして、命はつながっていきます。つながっていく未来の命のために、何を選び、食べるのかを考えるようになりました。一つオーガニックのものを手にしたことで、世界が広がり、楽しみがどんどん増えています。皆さんにも、これを機会に新しい楽しみを見つけてもらえると嬉しいです。<br>
