	<b>有機JASマークとは？</b><br>
	有機JASマークは、太陽と雲と植物をイメージしたマークです。<br>
	農薬や化学肥料などの化学物質に頼らないで、自然界の力で生産された食品を表しており、有機JAS規格に適合している農産物、加工食品、および畜産に付けることができます。公平な第三者である登録認定機関（農林水産大臣の登録を受けた機関）の認定を受けることが必要です。<br>
	<br>
	<b>オーガニック検査員とは？</b><br>
	農産物、加工食品、畜産物の生産者や事業者などに直接出向き、第三者の立場で客観的に検査を行い、登録認定機関に報告します。その検査結果をもとに、登録認定機関は認定の可否を判断します。オーガニックの現場にもっとも近い立場にあり、法律の改正や最新情報を常に把握しておく必要があります。
