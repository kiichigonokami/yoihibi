		<span><h3 class="article_detail_contents_box__title">食物アレルギーの悩みの中で出合ったオーガニック</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　私は、大人になってから発症した食物アレルギーに悩まされていた時期がありました。結婚して半年ぐらい経った頃から咳が止まらなくなり、夏風邪だと思っていたのですが、何件も病院を回り、ようやく食物アレルギーであることが分かりました。40年くらい前の話ですが、食物アレルギーの血液検査は普及していなかったため、医師の聞き取りと食事日記で原因を特定しました。その特定された食品だけでなく、食品添加物なども摂らないように指導され、当時は、有機はもちろん無農薬の作物もなく、自然食品店と称しているお店で、添加物のないものや、比較的少ないものを購入していました。特定されたアレルギーの食品を食べることを中止すると、体中に出ていた蕁麻疹が嘘のように消えていき、咳も止まり始めました。食物アレルギーって恐いと思いましたね。<br>
<br>
　そんな時、海外には「オーガニック」というものがあることを新聞で知り、デパートの地下食品売り場まで探しに行ったことを覚えています。それが私とオーガニックとの最初の出合いです。<br>
<br>
　ちょうどその頃、友人が「日本オーガニック検査員協会（JOIA）」の立ち上げに携わっていて、私もお手伝いをさせていただくことになりました。それから、もう20年もオーガニックの業界に携わっています。<br>
<br>

				</p>
			</li>

		</ul>
