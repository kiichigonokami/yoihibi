	<span><h3 class="article_detail_contents_box__title">「有機JASマーク」はオーガニックであることを保証するマーク</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　スーパーや農協などの食品売り場で、「私が作りました」という顔写真入りのPOPカードをよく見かけますね。生産者の顔が見えるという点では安心感があるかもしれません。最近ではいろいろな農法があり、おいしいものもたくさんあるとは思いますが、どんな肥料や農薬を使っているのか第三者によって検査されることもありませんし、情報公開もほとんどされていません。<br>
<br>
　一方、有機JASマークの食品は、法律で定められたルールに従って作っていて、有機であることが保証されています。生産者が記録している書類を私たちのようなオーガニック検査員が検査、確認していますので、いつでも情報公開することができます。そこが大きな違いであり、重要な点です。特に、加工食品の場合は、何か問題があった時に、原料にまでさかのぼることができ、原因を特定することができます。<br>
<br>
　また、有機JASマークをつけるための認定を取得しても、1年に1回、更新のために必ず検査を受けなければいけません。農産物の生産者や加工食品の製造現場の日々の積み重ねがあってこそ、マークを付け続けることができます。本当にまじめに取り組んでいらっしゃいます。
　最近では大手スーパーなどでもオーガニックの食品が手に入りやすくなりました。1ヵ月に一度でいいので、有機JASマークが付いているものを手に取っていただきたいです。<br>
<br>

				</p>
			</li>

		</ul>
