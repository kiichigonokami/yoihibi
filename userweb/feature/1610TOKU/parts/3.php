			<span><h3 class="article_detail_contents_box__title">“ゆるめる”効果がすごい</h3></span>
			<ul>


			  <li class="text_ver">
　高齢者の多くが抱える腰痛は冷えからくる「腎性腰痛」です。下肢が冷えると冷たい血液が腎臓に入って、腎臓の機能が落ちることによって起きる腰痛です。冷えると腰が痛いという方は、遠赤外線による温熱療法で熱エネルギーをからだに入れてあげればだいぶ楽になります。高年齢になると、どうしても体温が下がって血流が悪くなりますから、温めることを一番に考えるといいと思います。全身の血液の循環が良くなり、新陳代謝に必要な材料も運ばれ、回復も早くなります。骨折の治療の過程で温熱療法を用いると治りがかなり早いです。そして、夜中にトイレに起きる回数も減り、ぐっすり眠れるでしょう。<br>
<br>
　また、最近の研究で痛みの恐怖が脳に記憶されていることが分かってきました。すでに治っているのに頭の中で症状を固定しているため、慢性的な痛みが続いているという可能性もあります。このような場合にも、温めることは良い効果をもたらします。<u>気持ちが良くなりリラックスすると、緊張していた関節がゆるむうえに、脳もゆるんで痛みの感じ方も減ってきます。</u>マッサージでゴリゴリと強く揉まれた時の揉み返しは、毛細血管が切れてそれを修復しようと炎症が起きている状態で、その悪い情報は脳に記憶されてしまいます。現代人の多くは常態的に脳にストレスを抱えているので、そういう強い刺激ではなく、よりソフトでゆるめるような方法が今は必要です。<br>
<br>
　年齢を重ねると、生活習慣や姿勢のクセなどを変えるのは難しいことですが、温めることは誰でも簡単にできて、安全性が高い健康法だと私はおすすめします。<br>
