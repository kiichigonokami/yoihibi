			<span><h3 class="article_detail_contents_box__title">“痛み”とは</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　例えば腰痛の場合、レントゲンで何も問題がなくても痛いという人がいるかと思えば、明らかに異常があるのに全然痛くないという人もいます。本人の訴えと症状が合っていれば治療がしやすいのですが、そこが一致しないことが痛みを伴う症状には多く見られます。<br>
<br>
　肩こりは他の部位に何らかの原因のある場合が多く、肩にストレスがかかることによって動きが悪くなり、血液やリンパ液が溜まって肩こりが起きます。マッサージなどをすれば一時的に楽にはなりますが、原因が取り除かれたわけではないので、また繰り返します。<br>
<br>
　西洋医学は、血流が悪い、筋肉が硬いという状態を検査で確認することは得意ですが、なぜ？という原因を探ることはあまり得意ではありませんし、治療方法も限られています。<br>
<br>
<u>　痛みというのはからだからの信号、何かおかしいですよというからだからの訴えです。</u>その原因を探ることをせずに、安易に痛み止めの薬を飲み、その信号を消してしまうというのは、最初のうちはいいかもしれませんが、いつも原因不明のまま痛みだけを消していると、重大な病気を見逃してしまうことにもなりかねません。<br>
<br>


</p>
			  </li>

			</ul>
