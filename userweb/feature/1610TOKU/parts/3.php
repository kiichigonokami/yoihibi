		<div class="article_detail_contents_box">
			<span><h3 class="article_detail_contents_box__title">「偏り疲労」がゆがみ、痛みを招く</h3></span>
			<ul>


			  <li class="text_ver">
　長年の生活習慣や仕事をするための姿勢、パソコンの前に座りっぱなしで目を酷使するなど、からだの一部分だけ、片側だけを使う習慣があると、そこだけに負担がかかるため、ゆがみが発生します。それが、からだ全体のバランスにまで波及し「偏り疲労」を引き起こします。スポーツではテニスやゴルフは片側だけ、ウォーキングは下半身だけに負担がかかりますから、楽しむ分には良いですが、残念ながら健康のための運動とは言えません。慢性的な痛みの原因は、このような偏り疲労からきている場合も考えられます。健康に良いのは全身を使う運動です。水泳、ヨガ、ノルディックウォーキングなどがおすすめです。<br>
<br>
　「身体均整法」では、「平衡性（姿かたちのバランス）」「可動性（動きの範囲）」「強弱性（筋肉等の弾力性）」という３指針があります。人それぞれ生まれ持った先天的な身体的特徴12種類と職業や環境によって得られる後天的なものを判断して、ゆがみやずれを見ていきます。ヘルニアで手術をしなければいけないと言っていた方に、15分ぐらい施術すると嘘みたいに症状が消えたことがありました。<u>必ずしも真っすぐな姿勢がその人にとって健康な状態ではなく、自由度の高い楽な姿勢が良いのです。</u>しかしゆがみが大きくては生活に不便なので、３指針の均整をとることで、真っすぐにしようと言うのが均整術です。それが自然治癒力が高まる最も健康な状態で、痛みのない健康姿勢だと言えます。<br>
			  </li>

