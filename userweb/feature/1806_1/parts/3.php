		<div class="article_detail_contents_box">
			<span><h3 class="article_detail_contents_box__title">胸焼け、胃もたれ、食欲不振が三大不調</h3></span>
			<ul>
			  <li class="text_ver">
<p>
　胸がチリチリと焼けるような「胸焼け」は、胃の筋力の衰えが原因の一つです。筋力が弱ると、胃の入口部分のしまりが悪くなり、胃液が逆流しやすくなるのです。食べてすぐ横になると、より逆流しやすくなるので、食後3時間くらいは少しでも上半身を起こしておきましょう。<br>
 <br>
　また、加齢によって胃液の分泌量は減り、胃の働き自体もにぶくなるので、若い頃と同じ質、同じ形状の食事では、消化に時間がかかるようになります。「もたれ」は、そうして消化に時間がかかっている内容物が、胃にとどまっているときに起きます。さらに、胃液が減ると細菌やウイルスを胃で阻止しきれず腸に入りやすくなるため、腸内細菌のバランスが崩れ、免疫力が下がったり、だるさを感じやすくなったりもします。<br>
 <br>
　そんな胃腸を助けようと、胃腸薬を手放せない方も多いでしょう。しかし、胃腸薬は辛い症状を取り除くためのもので、日常的に頼り続けるものではありません。胃腸に負担をかけないために、消化しやすい食材を選ぶ、意識してよく噛むといった食生活の改善と、筋力を低下させない生活習慣の維持が第一の対策だと心得ておきましょう。<br>
 <br>
　食欲不振は、疾病などの特別な原因がない場合は、活動量の低下が関係していることが多くあります。高齢になると活動量や基礎代謝が落ちるため、若い頃ほどエネルギーを使いません。消化に時間がかかるうえにエネルギーを消費しないのですから、なかなかお腹が減らないのも当然です。
<br>


</p>
			  </li>
			  
			  
