			<span><h3 class="article_detail_contents_box__title">充実した食生活を送るためにできること</h3></span>
			<ul>
			
			  <li class="text_ver">
　食欲を刺激するには、活動量を増やすことが一番です。昼間によく動き、好奇心を持って脳を使うように心がければ、おのずと食欲もわきます。<br>
 <br>
　特別な原因がないのに食欲が落ちた方に試していただきたいのが食習慣の見直しです。一日の決まった時間に三度食べる習慣にこだわらず、お腹が減る感覚を優先してみてください。それによっては、一日二食にしてもよいのです。ただし、栄養不足や低栄養状態にならないようにバランスのよい食事を心がけ、質のよいサプリメントを積極的に利用しましょう。<br>
 <br>
　一方で、食欲を増す工夫もしたいところです。たとえば大葉や山椒などの香りは食を進めますし、お酒が体質に合う方は、食前酒のような飲み物も効果的です。<br>
 <br>
　また、からだの冷えは、胃腸の働きを低下させます。梅雨時から夏は、冷たいものを摂りすぎない、腹巻をするなど、からだの内から外からお腹周りを冷やさない工夫をしましょう。<br>
 <br>
　必要なときは胃腸を休ませる、ゆっくりよく噛む、食欲を増進させる工夫をするといった食生活の改善と、日中にしっかり活動する習慣を続けていけば、元気な胃腸を取り戻すことができます。


