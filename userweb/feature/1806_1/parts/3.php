			<span><h3 class="article_detail_contents_box__title">胃腸の不調を感じやすくなるわけは……</h3></span>
			<ul>

			  <li class="text_ver">
				<p>

　胃腸の消化のメカニズムは、若者も高齢者も変わりません。それなのに、以前は好物だった揚げ物に胸焼けしたり、ちょっとしたことで、もたれを感じたりすることが多くなっていませんか？ また、便秘や下痢といった腸の不調はどうでしょう。<br>
 <br>
　実は、胃や小腸、大腸といった臓器は筋肉でできています。ですから、加齢とともに足腰の筋肉が衰え、運動能力が下がっていくのと同じことが、胃腸の筋肉でも起こっています。たとえば便秘の原因は、腹筋が衰えて、いきむ力が弱くなることに加え、腸そのものが衰えて、働きがにぶくなることも考えられます。<br>
 <br>
　また、なんだか疲れやすくなったと感じたら、からだの外側だけでなく、内側の臓器も疲れやすくなっているものだと考えてください。<br>
 <br>
　疲労回復には休息が必要です。胃腸も同じで、疲れや不調を感じたら休ませることが、非常に大切な方法です。私も胃が重いときや、遅い時間に会食をしたときなどは、次の一食を抜いて胃腸を休ませています。<br>
 <br>
 <br>
 </p>
			  </li>
			</ul>
