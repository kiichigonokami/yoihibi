<p style="text-align: left; width: 250px;">
<b>胃腸の働きとは？</b><br>
胃の働きは大きく２つ。1つは消化を助けること。食べたものを消化液（胃液）で溶かしながら、細かく揉み砕きます。もう1つは、胃液に含まれる胃酸で、外部から入ってきた細菌やウイルスをやっつけること。
胃から十二指腸を通った内容物は小腸へ送られて栄養分が吸収されたのち、大腸で水分が吸収され、排泄できる形に整えられます。
</p>
