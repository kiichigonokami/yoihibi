	<span><h3 class="article_detail_contents_box__title">現代社会で多い病気の種類は？その筆頭は、やはり「がん」</h3></span>
	<ul>

			<li class="text_ver">
				<p>

　<span class="marker">環境や食生活の悪化、新たなストレスの増加、そして寿命が延びたこと。これらに伴って、病気の種類も変化してきました。</span>食や睡眠といった生活習慣の乱れに起因する血管や血液の病気、解毒を担っている肝臓や腎臓の病気、腸内環境の悪化などによる腸の病気やアレルギーなど。現代人は全身の臓器が疲れきっています。直接の死因には結びつかなくても、生活の質（QOL：Quality of Life）を下げる病気が近年になって急激に増えています（図1）。<br>
<br>
　そして、現代の病気の代表として挙げられるのは、やはり「がん」です。<span class="marker">がんになる方が増えているだけでなく、女性ホルモンが関係している婦人科系のがんなどが低年齢化してきています。</span><br>
