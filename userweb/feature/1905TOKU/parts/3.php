<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title">西洋医学で病気を治療するメリット・デメリットを知る</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　寿命が延びた現代、どんなに気をつけても病気は避けられなくなってきました。病気になった時、多くの方は一般的な西洋医学の病院で治療を受けますので、<span class="marker">病院の治療でできること、西洋医学のメリット・デメリットを知り、上手に活用することも、現代の健康管理にはとても必要なことです。</span><br>
<br>
　病院での治療とは、形や数値の異常を画像や血液の検査で調べ、異常な数値を薬で正常値に整える、悪いところを外科的な処置で取り除いたり整えたりする、痛みやかゆみなどの不快な症状を抑える、といった「形や数値を健常な状態にする」ことに主眼をおいています。これが病院でやるべきことであり、メリットと言えます。しかし西洋医学には、副作用というデメリットもあります。薬の副作用だけでなく、例えば胃がんを切除した結果、胃が小さくなり、「生活の質が低下する」ことも大きな副作用です。<br>
<br>
　医師が診るのは病気であって、患者さんの健康全般を網羅しているわけではありません。そこで皆さんに必要になるのは、病気やその原因を根本から見つめ直し、からだを整える「養生」という考え方です。外科医として15年、がんの切除術をしてきた私の経験から言っても、きちんとからだを整えてから手術を受けた人と、そうでない人とでは、手術の危険度も、術後の経過も、副作用の強さも変わることを実感しています。<br>
<br>
　一方で、西洋医学の治療をやめて、養生や自然療法だけで治そうとする患者さんも見てきました。この場合の問題は、その療法がよい結果につながっているかについて、客観的な評価を行わない方が多くいらっしゃることにあります。西洋医学には、腫瘍マーカーや超音波検査、血液や尿の数値といったさまざまな評価の方法があります。これを利用しない手はありません。<span class="marker">西洋医学と養生は、本来、相反するものではなく、どちらも必要で共存した方がいいと考えています。</span><br>
<br>

				</p>
			</li>

		</ul>
</div>

<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title">現代の健康管理に必要なのは養生、そして情報の取捨選択</h3></span>
		<ul>

			<li class="text_ver">
　病気を治すには医療と養生、どちらも必要であることをお話ししました。養生は病気の予防にも有効であり、日々の生活で自ら行えるものです。具体的に何をすればよいか迷ったときは、まずは、「昔ながらの生活」をイメージするとよいでしょう。日が昇ったら起きてからだをよく使い、日が沈んだら寝る。きちんと空腹になってから、季節に合った旬の自然な食材を使った料理を作り、食事はよく噛んで、腹八分目に食べる。そして、現代に必要な養生としては、例えば、からだの酸化を抑えるために意識して自然の多い場所に出掛ける、夏に腹巻きを身に着ける、からだを冷やさない食事を摂る、などを加えることをおすすめします。<br>
<br>
　もう1つ、現代の健康管理で肝心なことがあります。それは<span class="marker">知り得た情報や知識について、自分で判断し、行動できるようになること</span>です。現代の情報社会では日々膨大な情報が発信されており、玉石混交の中から正しいものを的確に選ぶのは至難の業です。私が考える信頼できる情報の目安は、<span class="marker">「その情報はお金を払って得たものか」「情報を発信している人は、その道を一途にやってきた経験者か」</span>、この2点です。病気になる前に、健康相談もできる医療者とのつながりを持つことも、これからはとても大切なことだと思います。<br>
<br>
　いざ体調不良になったら病院に行って薬をもらえばいい、ということでは、ここで得た情報が生かされたことにはなりません。養生、医療、情報、知識をバランスよく取り入れて、いいと思ったことは自ら行動を起こしてみましょう。<br>
<br>

