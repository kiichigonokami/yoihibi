		<span><h3 class="article_detail_contents_box__title">食習慣が病気を招く。客観的なデータが示す変化の恐さ</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　意外なデータを2つお見せします。1つは、日本とアメリカのがんによる死亡率の推移をグラフにしたものです（図2）。ご覧のとおり、日本ではがんの死亡率の上昇が続いていますが、アメリカでは1991年をピークに減ってきています。1995年を境に、がんで死亡する人はアメリカより日本のほうがどんどん多くなっています。<br>
<br>
　もう1つは、日米それぞれの野菜摂取量の推移を表したデータです（図3）。がんのグラフと反比例しているのは一目瞭然で、がんの死亡率と同じく、日米は1995年に逆転しています。和食は野菜が多くてヘルシー、アメリカの食事はジャンクで野菜が足りないと思われてきましたが、それはもはや一昔前のイメージです。<br>
<br>
　もちろん、がんの原因は食べ物だけではありませんが、これら2つのデータは、食習慣の変化ががんを誘発する原因の1つであることを示しています。<span class="marker">日本人は野菜不足を認識しなければいけません。食事の内容を見直すことは、がんやその他の病気の予防につながります。</span><br>
<br>

