		<span><h3 class="article_detail_contents_box__title">昔の生活と今の生活。変化がもたらす現代の健康リスク</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　「健康のためには昔と同じ生活をするのがよい」とよく言われます。現代においてこれは半分正しく、もう半分は適切ではないと私は考えます。なぜなら、現代と昔の生活では、大きく違う点が３つあるからです。<br>
<br>
　１つ目は、生活を取り巻く環境全般が昔より悪化していることです。人口が過密になり、自然が減り、大気汚染が進んでいます。食生活は一見豊かになりましたが、農薬が大量に使われ、不自然な加工や人工的な添加物で味付けされた食べ物が溢れ、本来の食の意味や役割が理想とかけ離れています。いつでもどこでも手軽に食べられるため、空腹を感じる時間がなく、食べ過ぎてしまうことも問題です。<br>
<br>
　２つ目は、昔には感じることがなかったストレスが増えていることです。絶えず何かに追われているような現代の生活。それに加え、デジタルツールの普及が新たなストレスも生み出しています。<br>
<br>
　３つ目は、寿命が大きく延びたことです。江戸時代の頃は人生五十年と言われたものですが、今や七十年、八十年は当たり前、最近では百二十歳を目指すとおっしゃる方もいるくらいです。<br>
<br>

				</p>
			</li>

		</ul>
</div>


