<div class="article_detail_contents_box">
		<span><h3 class="article_detail_contents_box__title">仕方がないとあきらめますか？</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　花粉のトラブルを軽くするのも、重くするのも、生活習慣や食生活が大きく関わっています。〝花粉モンスター〟を体内に入れないことが一番ですが、取り込んでしまったら、からだの解毒、排出システムがきちんと働くことが重要になってきます。<br>
<br>
　季節がらお花見などのイベントが続き、お酒を飲み過ぎてしまうと、アルコール代謝のために肝臓に負担がかかり、体内に入った花粉や有害物質の代謝（解毒・排出システム）に悪影響を与えることが考えられます。飲酒は控えめにした方が良さそうです。<br>
<br>
　また、年齢を重ねるにつれ便秘になる方が多くなり、それに伴って花粉のトラブルも増える傾向があります。皆さんは毎日のお通じはきちんとありますか？ 老廃物を排出できないと、血液を通して全身にトラブルの種を巡らせることにもなりかねません。便は生活習慣と食事の内容が表れます。現代人の便の量は戦前の約半分と言われていますが、それは根菜やいも類、豆類などに多く含まれる食物繊維の摂取量が減っていることと比例しています。消化の良いものばかりを食べていると、便の材料が不足し、出にくくなるため、便秘を招きやすくなります。さらに、野菜不足で栄養バランスの悪い食事は、腸内細菌の悪玉菌が優勢になり、腸の働きが悪化します。良い便を出すためには、善玉菌を優勢にする乳酸菌や発酵食品などを摂って腸内環境を整えることが大切です。<br>
<br>
　私たちが日常的にできることは、溜めない習慣を続けることです。花粉のトラブルは健康のバロメーターにもなります。今年だけでなく、来年、再来年のためにも、一つずつ自分のできることから始めましょう。<br>
<br>

				</p>
			</li>

		</ul>
