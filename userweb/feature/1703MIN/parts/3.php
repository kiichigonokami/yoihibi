		<span><h3 class="article_detail_contents_box__title">都会に潜む花粉モンスターに気を付けて</h3></span>
		<ul>

			<li class="text_ver">
				<p>

　およそ10年に一度実施されている国の調査によると、花粉のトラブルで通院している人は10年間でおよそ10％も増加しています。<br>
<br>
	花粉の飛散量が増えていることも原因の一つですが、最近の研究では、花粉と一緒にPM２・５や黄砂、化学物質など空気中の汚染物質が体内に入ることで、より強い反応が引き起こされることが分かっています。<br>
<br>
	東京都では、都心部の大田区の方が、秋川渓谷のある緑豊かなあきるの市より、花粉のトラブルを抱えている人が急増しています。<br>
<br>
　花粉とその他の汚染物質が合体した強力なアレルゲン〝花粉モンスター〟をからだに入れない、取り込まないことが対策の第一歩として欠かせません。<br>
