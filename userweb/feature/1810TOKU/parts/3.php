<br>
　『月のしずく』の最良の飲み方は、こまめに毎日続けること。一度にたくさん飲めばよいというわけではありません。朝昼晩、自然の恵みに感謝しつつ、気持ちを込めていただくような飲み方がベストのようですね。<br>
<br>
　以前、ある大企業の水質研究グループが、「ゆの里」のお水を「まるで相手に合わせて変化してくれるような水」と表現したことがあるそうです。『月のしずく』も、飲む人の健康状態に合わせて、あるいは気持ちに合わせて寄り添ってくれることでしょう。<br>
